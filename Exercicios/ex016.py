#Cria um programa que leia 5
#notas introduzidas pelo
#utilizador e que calcule a
#média aritmética entre eles.

num1 = float(input('Digite a nota 1 : '))
num2 = float(input('Digite a nota 2 : '))
num3 = float(input('Digite a nota 3 : '))
num4 = float(input('Digite a nota 4 : '))
num5 = float(input('Digite a nota 5 : '))

media = (num1 + num2 + num3 + num4 + num5) / 5

print('A média é de', media)